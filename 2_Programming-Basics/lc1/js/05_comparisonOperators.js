// Boolean Expressions: Comparison Operators
// <, <=, >, >=, ==,===, !=, !==

console.log(1 < 5); // true
console.log(1 <= 1); //true

console.log(1 > 5); //false
console.log(7 >= 7); //true

console.log("1" > 2); //false

console.log(1 == "1"); //true
console.log(1 === "1"); //false

console.log(1 != "1"); //false
console.log(1 !== "1"); //true

console.log(NaN == NaN);
